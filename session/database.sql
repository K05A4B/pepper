CREATE TABLE SESSION_DATA (
    SESS_ID CHARACTER(58), -- 生成的 session id 固定长度为58
    SESS_DATA VARBINARY(20971520), -- 最大储存 20MB 数据
    LIFE_CYCLE_START_TIME INTEGER(9223372036854775807), -- 生命周期开始时间
    LIFE_CYCLE_END_TIME INTEGER(9223372036854775807) -- 生命周期结束时间 (go 语言 int64 最大值 为9223372036854775807)
);